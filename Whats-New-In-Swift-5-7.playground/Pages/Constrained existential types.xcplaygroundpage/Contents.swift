# Constrained existential types

[SE-0353](https://github.com/apple/swift-evolution/blob/main/proposals/0353-constrained-existential-types.md) provides the ability to compose [SE-0309](https://github.com/apple/swift-evolution/blob/main/proposals/0309-unlock-existential-types-for-all-protocols.md) (“Unlock existentials for all protocols”) and [SE-0346](https://github.com/apple/swift-evolution/blob/main/proposals/0346-light-weight-same-type-syntax.md) (“Lightweight same-type requirements for primary associated types”) to write code such as `any Sequence<String>`.

It’s a huge feature in its own right, but once you understand the component parts hopefully you can see how it all fits together!

